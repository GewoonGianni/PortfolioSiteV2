        <h2>De 4 vormen van motivatie</h2>
        <h3>Intrinsieke motivatie</h3>
        <p>De meest bekende vorm van motivatie. Deze motivatie is de reden om een actie te ondernemen, met als reden dat
            je het leuk vindt.</p>
        <h3>Identificatie</h3>
        <p>Bij deze motivatie doe je iets omdat je bij een groep wilt horen. Je wilt jezelf een identiteit geven en
            kiest er daarom voor om deze actie te ondernemen.</p>
        <h3>Introjectie</h3>
        <p>Status, zowel positieve als negatieve status. Je wilt een status niet verliezen of juist een bepaald
            resultaat willen halen.</p>
        <h3>Extrinsieke motivatie</h3>
        <p>Iets willen doen om de "beloning" die volgt. Het kan waardering of erkenning opleveren. Dit komt vanaf
            buiten.</p>

        <br>

        <h2>Theorie</h2>
        <p>Motivatie is wat jou aanspoort iets te gaan doen. De 4 soorten zijn hierboven vermeld. Deze informatie komt
            grootendeels van controlandmotivate.nl, maar wordt ook ondersteund met informatie van dutch4healt.nl en
            leerling2020.nl.</p>

        <h3>Motivatie volgens Daniel Pink</h3>
        <p>Daniel Pink vertelt in een TED talk over wat motivatie is en dat wij het verkeerd gebruiken. Hij vertelt (met
            back-up van diverse onderzoeken) dat wij niet moeten motiveren met beloningen, maar dat mensen hun motivatie
            moeten halen uit wat ze doen. Ze gebruiken dus hun intrinsieke motivatie, en die motivatie halen ze uit het
            leuk of belangrijk vinden van hun project.</p>

        <h3>Motivatie en werk</h3>
        <p>Niet alleen op school is motivatie belangrijk, maar ook met werk. Ik ga nu aan de hand van het cariere tijger
            artikel (het andere artikel kon ik helaas niet openen) uitleggen hoe de werkvloer en de ideeën van Daniel
            Pink aan elkaar gelinkt worden.</p>
        <p>In dit artikel staat dat het belangrijk is voor bedrijven om het werk uitdagend te houden. Houd de doelen
            duidelijk en vermijd ten alle kosten vage doelen. Geef prestaties positieve aandacht en feedback. Het komt
            er dus op neer dat werk Intrinsieke motivatie moet leveren, wat ook het punt van Daniel Pink is.</p>

        <br>

        <h2>Prestatiemotivatie</h2>
        <h3>Mijn uitslag</h3>
        <p>Je bent in sterke mate intrinsiek én extrinsiek gemotiveerd. Dat je in sterke mate intrinsiek gemotiveerd
            bent betekent dat je het echt leuk vindt om te studeren. Je beleeft er plezier aan om beter en wijzer te
            worden. De kans dat jij iets opsteekt van school/je studie is dan ook groot. Dat je in sterke mate
            extrinsiek gemotiveerd bent wil zeggen dat je het ook belangrijk vindt dat je iets met je studie bereikt:
            een diploma, geld, status of de erkenning van de mensen waar je van houdt. Heel verstandig, want je leeft nu
            eenmaal niet in een sociaal isolement en moet straks wel je eigen boterham zien te verdienen.</P>
        <p>Tip: Blijf vooral goed naar jezelf luisteren. Je intrinsieke en extrinsieke motivatie zorgen er namelijk voor
            dat je op het goede spoor blijft en een studie of beroep kiest dat niet alleen uitstekend past bij je
            kwaliteiten, maar dat ook goed in de markt ligt.</p>
        <p>Ik ben het hier zelf zeer mee eens.</p>

        <br>

        <h2>Carrièreankers</h2>
        <h3>Gezelligheid</h3>
        <p>Ik vind het voor mijn carrière belangrijk dat ik een leuke gezellige tijd heb.</p>
        <h3>Veel geld verdienen</h3>
        <p>Als je veel geld hebt kun je fijn leven en leuke dingen doen. Ook kun je andere mensen helpen. Daarom vind ik
            dit belangrijk.</p>

